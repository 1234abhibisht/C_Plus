#include <iostream>
using namespace std;
int main() {
    char ch = 'a'; // for char datatype we use single colon
    // we can also store character in a string
    string a = "b"; // for string we use double colon
    cout << ch << " " << a;
    // for string a character we generally use char instead of string because string ends up taking too much space
    return 0;
}